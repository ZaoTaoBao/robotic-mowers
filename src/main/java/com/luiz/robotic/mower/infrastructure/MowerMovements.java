package com.luiz.robotic.mower.infrastructure;

/**
 * The possible letter send to mower are: L (Left), R (Right) and M(Move)
 */
public enum MowerMovements {

    /**
     * Left mower movements.
     */
    L,
    /**
     * Right mower movements.
     */
    R,
    /**
     * Move mower movements.
     */
    M
}
