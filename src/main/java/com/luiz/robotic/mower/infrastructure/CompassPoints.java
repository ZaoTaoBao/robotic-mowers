/**
 * The enum Compass points.
 * Four cardinal compass points (N, E, S, W)
 */
public enum CompassPoints {

    /**
     * North compass.
     */
    N,
    /**
     * East compass.
     */
    E,
    /**
     * South compass.
     */
    S,
    /**
     * West compass.
     */
    W
}
